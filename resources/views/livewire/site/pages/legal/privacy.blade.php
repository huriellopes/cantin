        <h2>2. Dados que coletamos</h2>
        <ul>
            <li><strong>Dados de cadastro</strong>: ao cadastrar um terreiro, pessoa ou entidade, coletamos nome, e-mail, telefone e endereço informados voluntariamente.</li>
            <li><strong>Dados de navegação</strong>: páginas acessadas e informações técnicas (data/hora, navegador), usadas para estatísticas de uso.</li>
            <li><strong>Cookies e identificadores</strong>: dados coletados por ferramentas de análise e publicidade <strong>somente mediante seu consentimento</strong> (veja a seção 4).</li>
        </ul>

        <h2>3. Finalidade do tratamento</h2>
        <ul>
            <li>Mapear e divulgar terreiros trans-inclusivos e entidades parceiras.</li>
            <li>Permitir contato entre as pessoas usuárias e os espaços cadastrados.</li>
            <li>Melhorar a experiência, medir audiência e, mediante consentimento, exibir/medir publicidade.</li>
        </ul>

        <h2>4. Cookies e consentimento</h2>
        <p>
            Utilizamos cookies essenciais ao funcionamento do site (que não exigem consentimento) e cookies de
            <strong>análise e publicidade</strong> (Google Analytics e Google Ads). Os cookies de análise e publicidade
            <strong>só são ativados se você clicar em "Aceitar"</strong> no banner de cookies. Você pode alterar sua escolha
            a qualquer momento limpando os dados do site no navegador.
        </p>

        <h2>5. Compartilhamento de dados</h2>
        <p>
            Não vendemos seus dados. Compartilhamos informações apenas com provedores que viabilizam o serviço
            (hospedagem e, mediante consentimento, ferramentas do Google para análise/publicidade), sempre limitados
            à finalidade descrita.
        </p>

        <h2>6. Seus direitos (art. 18 da LGPD)</h2>
        <p>Você pode, a qualquer momento, solicitar:</p>
        <ul>
            <li>confirmação da existência de tratamento e acesso aos seus dados;</li>
            <li>correção de dados incompletos, inexatos ou desatualizados;</li>
            <li>anonimização, bloqueio ou eliminação de dados desnecessários;</li>
            <li>portabilidade e informação sobre compartilhamento;</li>
            <li>revogação do consentimento e eliminação dos dados tratados com base nele.</li>
        </ul>

        <h2>7. Retenção e segurança</h2>
