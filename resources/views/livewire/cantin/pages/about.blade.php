                        acolhimento, resistência e transformação social. Mais do que um simples cadastro, o
                        <b>CaNTIn</b> é uma ferramenta que permite às pessoas <b>trans, travestis e não-binárias</b>
                        identificarem terreiros comprometidos com o respeito à sua identidade de gênero,
                        enquanto destaca lideranças religiosas e ações inclusivas em todo o país. Este projeto,
                        que conta com apoio governamental, reflete o compromisso dessas tradições religiosas com a
                        justiça social e a valorização da diversidade.
